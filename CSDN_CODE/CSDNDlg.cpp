//用户名在10个的位置随机
#define USER_MAX_LEN 12
BOOL CCSDNDlg::RandUser()
{
    //初始化用户名
    m_strUser = TEXT("");
    
    for(int i = 0; i < USER_MAX_LEN; i++)
    {
        m_strUser += m_Assist.GetRand();
    }

    return TRUE;
}


